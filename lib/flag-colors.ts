const FALLBACK: readonly [string, string] = ["#3b82f6", "#38bdf8"]

/** Two flag-inspired colors for subtle UI washes (hex). */
export function getFlagColors(iso2: string | null | undefined): readonly [string, string] {
  if (!iso2) return FALLBACK
  return FLAG_COLORS[iso2.toUpperCase()] ?? FALLBACK
}

/** CSS linear-gradient string — very low opacity flag wash. */
export function flagWashGradient(
  iso2: string | null | undefined,
  angle = "to bottom right",
): string {
  const [a, b] = getFlagColors(iso2)
  return `linear-gradient(${angle}, color-mix(in srgb, ${a} 14%, transparent), transparent 48%, color-mix(in srgb, ${b} 11%, transparent))`
}
